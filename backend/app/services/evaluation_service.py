import os
import re
import json
import requests
from typing import List, Dict, Any
from app.services.embedding_service import embedding_service
from app.services.vector_store import vector_store

OLLAMA_URL = os.getenv("OLLAMA_URL", "http://localhost:11434")
OLLAMA_MODEL = os.getenv("OLLAMA_MODEL", "llama3")

EVALUATION_CRITERIA = [
    {
        "category": "Legal",
        "criteria": "Is the project in line with the sector regulations?",
        "weight": 4
    },
    {
        "category": "Financing",
        "criteria": "Is each of the project activities costed?",
        "weight": 1
    },
    {
        "category": "Financing",
        "criteria": "Will the operational costs of the project be funded from the revenue generated by the project?",
        "weight": 2
    },
    {
        "category": "Consultation",
        "criteria": "Have the project beneficiaries been consulted during project formulation?",
        "weight": 1
    },
    {
        "category": "Consultation",
        "criteria": "Have the project stakeholders been consulted during project formulation?",
        "weight": 2
    },
    {
        "category": "Impact",
        "criteria": "Is the project environmentally friendly?",
        "weight": 2
    }
]

def generate_ollama_completion(prompt: str) -> Dict[str, Any]:
    """
    Sends a generation request to the local Ollama service using the target model.
    Forces JSON resolution.
    """
    url = f"{OLLAMA_URL}/api/generate"
    payload = {
        "model": OLLAMA_MODEL,
        "prompt": prompt,
        "stream": False,
        "format": "json"
    }

    try:
        response = requests.post(url, json=payload, timeout=60)
        response.raise_for_status()
        
        result_json = response.json()
        raw_response_text = result_json.get("response", "{}")
        
        return json.loads(raw_response_text)
    except requests.exceptions.RequestException as e:
        print(f"Error communicating with Ollama: {e}")
        return {"decision": "NO", "reason": f"Failed to connect to Ollama API: {str(e)}"}
    except json.JSONDecodeError as e:
        print(f"Ollama returned invalid JSON: {e}")
        return {"decision": "NO", "reason": "Model evaluation output was unparseable"}


def evaluate_proposal(proposal_id: str) -> Dict[str, Any]:
    """
    Evaluates a specific proposal against static static criteria.
    For each criterion, it searches for contextual chunks and calls the Ollama engine.
    """
    evaluation_results = []
    total_score = 0
    max_score = sum(c["weight"] * 3 for c in EVALUATION_CRITERIA)

    top_k_chunks = 5
    
    for criterion in EVALUATION_CRITERIA:
        # Create a search embedding of the static criterion question
        query_embedding = embedding_service.embed_text(criterion["criteria"])
        
        # Contextual search against the specified document only
        search_results = vector_store.search(
            query_embedding=query_embedding, 
            top_k=top_k_chunks, 
            where={"proposal_id": proposal_id}
        )
        
        # Concatenate retrieved vector texts
        retrieved_texts = "\n\n---\n\n".join(
            [res["text"] for res in search_results]
        ) if search_results else "No relevant context found in this document."

        # Assemble prompt according to constraints
        prompt = f"""You are an expert project reviewer.

Evaluate the following criterion based on the project proposal excerpts.

Criterion:
{criterion['criteria']}

Proposal excerpts:
{retrieved_texts}

Scoring rules:
3 = Fully satisfies the criterion
2 = Mostly satisfies the criterion
1 = Partially satisfies
0 = Weak or unclear evidence
-1 = Poor evidence
-2 = Completely fails the criterion

Answer ONLY in JSON format:
{{
  "score": number,
  "confidence": number between 0 and 1,
  "reason": "short explanation",
  "evidence": "direct excerpt from the proposal"
}}
"""
        
        # Query Ollama
        llm_response = generate_ollama_completion(prompt)
        
        # Extract variables
        raw_score = llm_response.get("score", 0)
        confidence = llm_response.get("confidence", 0.0)
        reason = str(llm_response.get("reason", "No reason provided."))
        evidence = str(llm_response.get("evidence", "No evidence provided."))
        
        # Force strict bounding constraints around AI behavior
        try:
            raw_score = int(raw_score)
            raw_score = max(-2, min(3, raw_score))
        except (ValueError, TypeError):
            raw_score = 0
            
        try:
            confidence = float(confidence)
            confidence = max(0.0, min(1.0, confidence))
        except (ValueError, TypeError):
            confidence = 0.0
        
        criterion_score = raw_score * criterion["weight"]
        total_score += criterion_score
        
        # Record structured iteration report
        evaluation_results.append({
            "category": criterion["category"],
            "criteria": criterion["criteria"],
            "score": raw_score,
            "confidence": confidence,
            "reason": reason,
            "evidence": evidence,
            "weight": criterion["weight"],
            "total": criterion_score
        })

    return {
        "project_name": f"Proposal {proposal_id}",
        "results": evaluation_results,
        "total_score": total_score,
        "max_score": max_score
    }
